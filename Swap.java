public class Swap {
    public static void main(String[] args) {
        int a, b, temp;
        a = 2;
        b = 3;
        System.out.println(a);
        System.out.println(b);

        temp = a;
        a = b;
        b = temp;

        System.out.println(a);
        System.out.println(b);


    }
}
